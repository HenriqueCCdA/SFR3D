/********************************************************************* 
 * LOADSENDPROD :                                                    * 
 * ((a1*sin(w1*x1+c1))*(a2*sin(w2*x2+c2)*(a3*sin(w3*x3+c3))          * 
 *-------------------------------------------------------------------* 
 * Parametros de entrada:                                            * 
 *-------------------------------------------------------------------* 
 * tA     -> nao definido                                            * 
 * par    -> parametros                                              * 
 * xm     -> ponto (x1,x2,x3)                                        * 
 *-------------------------------------------------------------------* 
 * Parametros de saida:                                              * 
 *-------------------------------------------------------------------* 
 * tA     -> valor calculado da funcao                               * 
 *-------------------------------------------------------------------* 
 * OBS:                                                              * 
 *-------------------------------------------------------------------* 
 *********************************************************************/
void loadSenProd(DOUBLE *tA,DOUBLE *par,DOUBLE *xm){

  DOUBLE a1,a2,a3,w1,w2,w3,c1,c2,c3;

/*... a1*/
  a1 = par[0];
/*... w1*/
  w1 = par[1];
/*... c1*/
  c1 = par[2];
/*... a2*/
  a2 = par[3];
/*... w2*/
  w2 = par[4];
/*... c2*/
  c2 = par[5];
/*... a3*/
  a3 = par[6];
/*... w3*/
  w3 = par[7];
/*... c3*/
  c3 = par[8];
/*...*/
  tA[0] = a1*sin(w1*xm[0]+c1)
         *a2*sin(w2*xm[1]+c2) 
         *a3*sin(w3*xm[2]+c3);
/*...................................................................*/
} 
