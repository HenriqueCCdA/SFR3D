/********************************************************************* 
 * BUBBLESORT :  ordena o arranjo em ordem crescente                 * 
 *-------------------------------------------------------------------* 
 * Parametros de entrada:                                            * 
 *-------------------------------------------------------------------* 
 * sja    -> arranjo                                                 * 
 * dja    -> arranjo                                                 * 
 * n      -> dimensao do arranjo                                     * 
 *-------------------------------------------------------------------* 
 * Parametros de saida:                                              * 
 *-------------------------------------------------------------------* 
 * sja    -> arranjo ordenado em funcao de dja                       * 
 *-------------------------------------------------------------------* 
 * OBS:                                                              * 
 *-------------------------------------------------------------------* 
 *********************************************************************/ 
void v2Bubblesort(INT *sja,INT *dja,INT n){
  
  INT i;
  INT sj;
  bool itroca;
  
  
  do{
    itroca=false;
    for(i=1;i<n;i++){
      if(dja[sja[i]]   < dja[sja[i-1]]){
         
         sj       = sja[i-1];
         sja[i-1] = sja[i];
         sja[i]   = sj;
         
         itroca  = true;
      }
    }
  }while(itroca);

}
