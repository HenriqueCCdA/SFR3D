/********************************************************************* 
 * BUBBLESORT :  ordena o arranjo em ordem crescente                 * 
 *-------------------------------------------------------------------* 
 * Parametros de entrada:                                            * 
 *-------------------------------------------------------------------* 
 * ja     -> arranjo                                                 * 
 * n      -> dimensao do arranjo                                     * 
 *-------------------------------------------------------------------* 
 * Parametros de saida:                                              * 
 *-------------------------------------------------------------------* 
 * ja     -> arranjo ordenado                                        * 
 *-------------------------------------------------------------------* 
 * OBS:                                                              * 
 *-------------------------------------------------------------------* 
 *********************************************************************/ 
void bubblesort(INT *ja,INT n){
  
  INT i,j;
  bool itroca;
  
  do{
    itroca=false;
    for(i=1;i<n;i++){
      if(ja[i]   < ja[i-1]){
         j       = ja[i-1];
         ja[i-1] = ja[i];
         ja[i]   = j;
         itroca  = true;
      }
    }
  }while(itroca);

}
